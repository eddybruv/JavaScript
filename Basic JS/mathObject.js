/**
 * The math object has no constructor, it is static and all methods
 * and properties can be used without creating a math object first.
 * Math properties are constants
 */

//Math Properties
console.log(Math.PI) //returns PI
console.log(Math.E) //returns Euler's Number
console.log(Math.SQRT2) //returns the square root of 2
console.log(Math.LN2) //returns the natural logarithm of 2

//Math Methods
Math.round(78.3); //returns 78
Math.round(89.7); //returns 90

